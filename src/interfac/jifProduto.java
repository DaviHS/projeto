         if (dificuldade == 1){
             jrFacil.setSelected(true);
         }
            if (dificuldade == 2){
                jrMedio.setSelected(true);
            }
              if (dificuldade == 3){
                  jrDificil.setSelected(true);
              }
              
         jtFund.setText(objpro.getPro_fundi());
         jtSlitter.setText(objpro.getPro_fundi());
         jtPrensa.setText(objpro.getPro_fundi());
         
         apaga=objpro.getPro_apaga09();
         if (apaga == 1){
             jc009.setSelected(true);
         }
         
         raa=objpro.getPro_raa();
        if(raa==1){
            jrLado1.setSelected(true);
        } if(raa==2){
           jrLado2.setSelected(true);
        } 
        
       estoMin=objpro.getPro_estoMin();
    //int estoMin;
        if(estoMin==1){
                jcEstoMin.setSelected(true);   
                  } if(estoMin==0){
                      jcEstoMin.setSelected(false);
                  }
        
        calha=objpro.getPro_calha();
        // int calha;
        if(calha==1){
            jcCalha.setSelected(true);
                  } if(calha==0){
                      jcCalha.setSelected(false);
                  }
        
        produ=objpro.getPro_produtivo();
            //int produ;
            if(produ==1){
                    jcProdu.setSelected(true);
                   } if(produ==0){
                      jcProdu.setSelected(false);
                  }
            
            ina=objpro.getPro_inativo();
            //int ina;
            if(ina==1){
               jcIna.setSelected(true); 
            } if(ina==0){
                jcIna.setSelected(false);
            }
         
         
         
