    private void jbCapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCapActionPerformed
        // TODO add your handling code here:                     
        int row =jTabela.getSelectedRow();
        
        if (jPrincipal.fichao==true){
            objpro.setPro_descri(String.valueOf(jTabela.getValueAt(row, 1).toString().trim()));
            jifFichao.recebeProduto(objpro);
            
        } else {
            objpro.setId_produtos(Integer.parseInt(jTabela.getValueAt(row, 0).toString().trim()));
            jifProduto.recebeidProdutos(objpro);
        }
        dispose(); 
    }//GEN-LAST:event_jbCapActionPerformed

