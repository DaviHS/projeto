    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        // TODO add your handling code here:   
        
        DefaultTableModel mp1 = (DefaultTableModel) jTabela.getModel();
      
      int l=mp1.getRowCount();
      
      
      if(l>0){
          while(l>0){
                ((DefaultTableModel) jTabela.getModel()).removeRow(l-1); // primeira linha tem valor 0
                l--;
            } //fim While
            
        } //fim do if - verifica se tem alguma info. na tabela
      
      //fim limpa a tabela
        
        String tipo="";
      String escolha=jcTipo.getSelectedItem().toString().trim();
      
            if(escolha.equals("Cod.")){
          tipo=" "+"id_produtos";
      }     if(escolha.equals("Descrição")){
          tipo=" "+"pro_descri";
      }    if(escolha.equals("Grupo")){
          tipo=" "+"pro_grupo";
      }     if(escolha.equals("Linha")){
          tipo=" "+"pro_linha";
      }     if(escolha.equals("Material")){
          tipo=" "+"pro_material";
      }     if(escolha.equals("Dureza")){
          tipo=" "+"pro_dureza";
      }         
      String arg=jtInfo.getText();
      
      

        
        
      try{
          ResultSet rs = ConsultaDAO.carregaTelaProdutos(tipo, arg);
          
           DefaultTableModel mp = (DefaultTableModel) jTabela.getModel();
            
            while(rs.next()){
                
                //coleta os dados da linha no banco de dados
                String Coluna0=rs.getString("id_produtos").toString().trim(); 
                String Coluna1=rs.getString("pro_descri").toString().trim();
                String Coluna2=rs.getString("pro_grupo").toString().trim();
                String Coluna3=rs.getString("pro_linha").toString().trim();
                String Coluna4=rs.getString("pro_material").toString().trim();
                String Coluna5=rs.getString("pro_dureza").toString().trim();
                                
                                
          mp.addRow(new String[]{Coluna0,Coluna1,Coluna2,Coluna3,Coluna4,Coluna5});
          
          
          
      }
        
      } catch (SQLException error){
          JOptionPane.showMessageDialog(this, "Ocorreu um erro: "+ error, "Preencher Item", 2);
      }
      
      tamanhocoluna();
        jTabela.setAutoCreateRowSorter(true); //ordena as linhas ao clicar na coluna
    }//GEN-LAST:event_jbPesquisarActionPerformed

