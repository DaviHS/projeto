    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        int i = jTabela.getSelectedRow();

        jtCodLinha.setText(jTabela.getModel().getValueAt(i, 69).toString());

        if (jtCodLinha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecione um linha! ");
        } else {
            int codigo = Integer.parseInt(jtCodLinha.getText());

            EXDAO = new ExcluirDAO();

            try {
                if (JOptionPane.showConfirmDialog(this, "Confirmar Exclusão? ", "Confirmação",
                        JOptionPane.YES_NO_CANCEL_OPTION) == JOptionPane.YES_NO_OPTION) {
                    if (EXDAO.ExcluirLinhaFichao(codigo)) {
                        JOptionPane.showMessageDialog(this, "Excluido com sucesso!");

                    } else {

                        JOptionPane.showMessageDialog(this, "Erro ao excluir o registro! ");

                    }
                } else {

                    JOptionPane.showMessageDialog(this, "Operação cancelada! ");

                }

            } catch (SQLException error) {
                JOptionPane.showMessageDialog(this, "Erro: Exclusão Não Realizada!" + error.getMessage());
            }
        }

        DefaultTableModel row = (DefaultTableModel) jTabela.getModel();
        row.removeRow(jTabela.getSelectedRow());
        salvar();


    }//GEN-LAST:event_jbExcluirActionPerformed

